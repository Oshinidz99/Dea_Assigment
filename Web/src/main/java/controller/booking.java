    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
      
        
        Connection con = null;
        con = dbconnect.openconnection();
        
        String name = request.getParameter("name");
        String phone_number = request.getParameter("phone_number");
        String email = request.getParameter("email");
        String time = request.getParameter("time");
        String guest = request.getParameter("guest");
        
        try {
            PreparedStatement stmt= con.prepareStatement("INSERT INTO booking(idbooking,name,phone_number,email,time,guest) VALUES(null,?,?,?,?,?)",PreparedStatement.RETURN_GENERATED_KEYS);
            stmt.setString(1, name);
            stmt.setString(2, phone_number);
            stmt.setString(3, email);
            stmt.setString(4, time);
            stmt.setString(5, guest);
            stmt.executeUpdate();
            
        } catch (SQLException ex) {
           
                    
        }
    }

